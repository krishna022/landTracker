export type TranslationKey = keyof typeof translations.en;

// RTL language codes
export const RTL_LANGUAGES = ['ar', 'he', 'fa', 'ur'] as const;

// Check if current language requires RTL layout
export const isRTL = (languageCode: string): boolean => {
  return RTL_LANGUAGES.includes(languageCode as typeof RTL_LANGUAGES[number]);
};

export const useTranslation = () => {
  const { preferences } = usePreferences();
  const languageCode = preferences.language.code;

  const t = (key: string): string => {
    const translationsForLanguage = translations[languageCode as keyof typeof translations] || translations.en;
    return (translationsForLanguage as any)[key] || (translations.en as any)[key] || key;
  };

  return { 
    t, 
    language: preferences.language,
    isRTL: isRTL(languageCode)
  };
};
